-----------------------TRANSFERENCIA INTERNA ------------------------------------------------
SELECT
 tipo_transferencia
,Count(tipo_transferencia)  QTD

FROM(SELECT
  A.CD_ATENDIMENTO
 ,To_Char (A.DT_ATENDIMENTO ,'DD/MM/YYYY') DT_ATENDIMENTO
 ,a.hr_atendimento
 ,L.DS_LEITO
 ,L.CD_UNID_INT
 ,P.NM_PACIENTE
 ,To_Char (dt_nascimento , 'dd/mm/yyyy') dt_nascimento
 ,M.CD_LEITO_ANTERIOR
 ,L2.DS_LEITO LEITO_ANTERIOR
 ,L2.CD_UNID_INT CD_UNID_INT_ANTERIOR
 , CASE WHEN L2.CD_UNID_INT IN (37,28,23,8,9,10,11,12,13,40,15) AND L.CD_UNID_INT IN (37,28,23,8,9,10,11,12,13,40,15) THEN 'NAO E TRANSFERENCIA UI'
        WHEN L2.CD_UNID_INT IN (38,3,25,7,26,27,31,34,16,30,45) AND L.CD_UNID_INT IN (38,3,25,7,26,27,31,34,16,30,45) THEN 'NAO E TRANSFERENCIA UTI'
        WHEN L2.CD_UNID_INT IN (37,28,23,8,9,10,11,12,13,40,15) AND L.CD_UNID_INT IN (38,3,25,7,26,27,31,34,16,30,45) THEN 'E TRANSFERENCIA UTI'
        WHEN L2.CD_UNID_INT IN (38,3,25,7,26,27,31,34,16,30,45) AND L.CD_UNID_INT IN (37,28,23,8,9,10,11,12,13,40,15) THEN 'É TRANSFERENCIA UII' END TIPO_TRANSFERENCIA


 FROM  ATENDIME A
      ,LEITO  L
      ,PACIENTE P
      ,MOV_INT M
      ,LEITO L2
      ,UNID_INT UIT


WHERE A.CD_ATENDIMENTO = M.CD_ATENDIMENTO
  AND M.CD_LEITO = L.CD_LEITO
  AND L.CD_UNID_INT = UIT.CD_UNID_INT
  AND M.CD_LEITO_ANTERIOR = L2.CD_LEITO (+)
  AND A.CD_PACIENTE = P.CD_PACIENTE
  AND A.TP_ATENDIMENTO = 'I'
  AND M.TP_MOV = 'O' -- TRANSFERENCIA DE LEITOS
  AND L.TP_SITUACAO = 'A'
  AND L.CD_UNID_INT IN (38,3,25,7,26,27,31,34,16,30,37,28,23,45,8,9,10,11,12,13,40,15)-- UNIDADES DE INTERNÇAO UI E UTI
  AND L2.CD_UNID_INT IN (38,3,25,7,26,27,31,34,16,30,37,28,23,45,8,9,10,11,12,13,40,15)-- UNIDADES DE INTERNÇAO UI E UTI
  AND To_date(a.hr_atendimento,'dd/mm/yyyy hh24:mi') BETWEEN To_Date (SYSDATE,'dd/mm/yyyy hh24:mi') AND To_Date (SYSDATE,'dd/mm/yyyy')
-- ORDER BY a.hr_atendimento desc

 )

WHERE tipo_transferencia IN('E TRANSFERENCIA UTI', 'E TRANSFERENCIA UI')
GROUP BY tipo_transferencia

